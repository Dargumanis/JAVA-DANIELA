    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (!(!idField.getText().equals("") && !dniField.getText().equals("") && !nameField.getText().equals("") && !surnameField.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Todos los datos deben tener un valor.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JOptionPane.showConfirmDialog(null, "¿Seguro que quiere modificar este usuario?", "Modificar", JOptionPane.YES_NO_OPTION);

    }//GEN-LAST:event_updateButtonActionPerformed

